'''
思考：要存储100个人的年龄
解决：使用列表
本质：一种有序的集合
功能：有序存储大量的数据
'''

'''创建列表
格式：列表名 = [列表选项1, 列表选项2, ... , 列表选项n]
特点：列表中的元素可以是不同的数据类型
'''

list = [1, 2, 3, 4, 4]
list1 = [1, "asd", "ss", "dfdf", 2231]
print(list)
print(list1)

'''列表元素的访问

取值 格式：列表名[下标]
注意：不要越界（下标不能超出了可表示的范围）
'''
list2 = ["sda", 12, "asd", 2222]
print(list2[1])

